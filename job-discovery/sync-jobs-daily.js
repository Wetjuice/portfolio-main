#!/usr/bin/env node
/**
 * Daily Job Sync Script
 * 
 * Runs daily to:
 * 1. Fetch new jobs from APIs
 * 2. Deduplicate against existing jobs
 * 3. Filter out jobs user has already archived
 * 4. Update jobs-data.js with net-new jobs only
 * 5. Commit and push changes
 * 
 * Usage: node sync-jobs-daily.js
 */

const { discoverJobs } = require('./discover-jobs-v2.js');
const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const JOBS_DATA_FILE = path.join(__dirname, 'jobs-data.js');
const DECISIONS_SYNC_FILE = path.join(__dirname, 'user-decisions.json');

// Location filter (Remote US + Seattle/WA)
function isLocationEligible(location) {
    const loc = location.toLowerCase();
    
    if (loc.includes('remote')) {
        if (loc.includes('us') || loc.includes('u.s.') || loc.includes('united states')) {
            return true;
        }
        if (loc === 'remote') {
            return true;
        }
        if (loc.includes('/')) {
            return true;
        }
    }
    
    if (loc.includes('seattle') || loc.includes('washington') || loc.includes(' wa')) {
        return true;
    }
    
    return false;
}

// Load user decisions (synced from localStorage via browser script)
function loadUserDecisions() {
    if (!fs.existsSync(DECISIONS_SYNC_FILE)) {
        return { decisions: {} };
    }
    try {
        return JSON.parse(fs.readFileSync(DECISIONS_SYNC_FILE, 'utf8'));
    } catch (e) {
        console.warn('⚠️  Failed to load user decisions:', e.message);
        return { decisions: {} };
    }
}

// Load current jobs from jobs-data.js
function loadCurrentJobs() {
    if (!fs.existsSync(JOBS_DATA_FILE)) {
        return [];
    }
    
    try {
        const content = fs.readFileSync(JOBS_DATA_FILE, 'utf8');
        // Extract initialJobs array via regex (hacky but avoids eval)
        const match = content.match(/const initialJobs = (\[[\s\S]*?\]);/);
        if (match) {
            return JSON.parse(match[1]);
        }
    } catch (e) {
        console.warn('⚠️  Failed to parse existing jobs:', e.message);
    }
    
    return [];
}

// Main sync logic
async function syncJobs() {
    console.log('🔄 Starting daily job sync...\n');
    
    // Step 1: Fetch new jobs from APIs (v2 scraper does filtering)
    console.log('📥 Fetching curated jobs from APIs (location + role filters applied)...');
    const fetchedJobs = await discoverJobs(); // Returns pre-filtered jobs
    console.log(`   Found ${fetchedJobs.length} curated jobs from APIs\n`);
    
    // Step 2: Location filtering already done by v2 scraper
    const locationFiltered = fetchedJobs; // Already filtered at source
    
    // Step 3: Load existing jobs
    const existingJobs = loadCurrentJobs();
    const existingIds = new Set(existingJobs.map(j => j.id));
    console.log(`📋 Loaded ${existingJobs.length} existing jobs\n`);
    
    // Step 4: Load user decisions
    const { decisions } = loadUserDecisions();
    const archivedIds = new Set(
        Object.entries(decisions)
            .filter(([id, status]) => status === 'archived')
            .map(([id]) => id)
    );
    console.log(`🗄️  ${archivedIds.size} jobs previously archived by user\n`);
    
    // Step 5: Find net-new jobs (not in existing, not archived)
    const newJobs = locationFiltered.filter(job => 
        !existingIds.has(job.id) && !archivedIds.has(job.id)
    );
    console.log(`✨ ${newJobs.length} net-new jobs to add\n`);
    
    if (newJobs.length === 0) {
        console.log('✓ No new jobs to add. Exiting.');
        return { added: 0, total: existingJobs.length };
    }
    
    // Step 6: Merge new jobs with existing (new jobs first)
    const mergedJobs = [...newJobs, ...existingJobs];
    
    // Step 7: Write updated jobs-data.js
    const fileContent = `// Auto-generated by sync-jobs-daily.js on ${new Date().toISOString()}
// ${mergedJobs.length} verified active postings

const initialJobs = ${JSON.stringify(mergedJobs, null, 4)};

// Location eligibility check
function isLocationEligible(location) {
    const loc = location.toLowerCase();
    
    // Remote positions that specify US or have 'remote' without foreign country
    if (loc.includes('remote')) {
        // Explicit remote US
        if (loc.includes('us') || loc.includes('u.s.') || loc.includes('united states')) {
            return true;
        }
        // Just 'Remote' with no other location (assume US)
        if (loc === 'remote') {
            return true;
        }
        // 'Location / Remote' or 'Remote / Location' format with US cities
        if (loc.includes('/')) {
            return true; // Allow hybrid remote positions
        }
    }
    
    // Seattle/WA area (in-office or hybrid)
    if (loc.includes('seattle') || loc.includes('washington') || loc.includes(' wa')) {
        return true;
    }
    
    return false;
}

// Add estimated salary data (APIs don't typically expose this)
initialJobs.forEach(job => {
    // Skip if salary already set
    if (job.salary) return;
    
    const title = job.title.toLowerCase();
    
    // Estimate based on role level
    if (title.includes('director') || title.includes('vp') || title.includes('head of')) {
        job.salary = '$250K - $400K';
    } else if (title.includes('principal') || title.includes('staff')) {
        job.salary = '$220K - $320K';
    } else if (title.includes('senior')) {
        job.salary = '$180K - $280K';
    } else {
        job.salary = '$150K - $250K';
    }
});

// Filter to location-eligible jobs only
const eligibleJobs = initialJobs.filter(job => isLocationEligible(job.location));
`;
    
    fs.writeFileSync(JOBS_DATA_FILE, fileContent);
    console.log(`📝 Updated ${JOBS_DATA_FILE} with ${mergedJobs.length} total jobs\n`);
    
    // Step 8: Commit and push
    try {
        execSync('git add jobs-data.js', { cwd: __dirname });
        execSync(`git commit -m "Daily job sync: +${newJobs.length} new jobs (${mergedJobs.length} total)"`, { cwd: __dirname });
        execSync('git push', { cwd: __dirname });
        console.log('✓ Changes committed and pushed to GitHub\n');
    } catch (e) {
        console.warn('⚠️  Git operations failed:', e.message);
    }
    
    // Step 9: Print summary
    console.log('📊 Summary:');
    console.log(`   New jobs added: ${newJobs.length}`);
    console.log(`   Total jobs in app: ${mergedJobs.length}`);
    console.log(`   Jobs archived by user: ${archivedIds.size}`);
    
    if (newJobs.length > 0) {
        console.log('\n🆕 New jobs:');
        newJobs.forEach((job, i) => {
            console.log(`\n${i + 1}. ${job.title}`);
            console.log(`   ${job.company} · ${job.location}`);
            console.log(`   ${job.url}`);
        });
    }
    
    return { added: newJobs.length, total: mergedJobs.length };
}

// CLI execution
if (require.main === module) {
    syncJobs().catch(err => {
        console.error('❌ Error:', err);
        process.exit(1);
    });
}

module.exports = { syncJobs };
